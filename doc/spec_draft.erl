%%%=============================================================================
%%% informal type declarations
%%%=============================================================================

-type ehtml5() :: [tag()]
                | [tag(), content()]
                | [tag(), attrs()]
                | [tag(), attrs(), content()]
                | [tag(), ehtml5()]
                | [tag(), [ehtml5()]]
                | [tag(), attrs(), ehtml5()]
                | [tag(), attrs(), [ehtml5()]]
                | [ehtml5()].

-type tag()     :: atom().
-type attrs()   :: map(). % #{ atom() => binary() }
-type content() :: binary().

%%%=============================================================================
%%% ehtml5:render/1
%%%=============================================================================

-spec render(ehtml5()) -> iolist().
